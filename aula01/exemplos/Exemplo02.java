package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        int numero; // declaração

        numero = 10; // atribuição

        int numero2 = 20; // declaração com atribuição

        System.out.println(numero);

        System.out.println(numero2);

        numero = 5;

        System.out.println(numero);

        numero = 7 * 2;

        System.out.println(numero);

        numero = numero + 1;

        System.out.println(numero);
    }
}
