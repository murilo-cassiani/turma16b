package exercicios;

/*

Operadores aritméticos: + - * /

*/

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("Operações com 15 e 7:");
        System.out.println("Soma: " + (15 + 7));
        System.out.println("Subtração: " + (15 - 7));
        System.out.println("Multiplicação: " + (15 * 7));
        System.out.println("Divisão: " + (15 / 7));
        System.out.println("Divisão: " + (15.0 / 7));
    }
}
